							<div class="tab-pane fade in active" id="tab-bottom1">
								<p class="lead">African Parks, organisme de conservation à but non lucratif et activement engagé dans la conservation de la faune et de la flore, gère, en collaboration avec les autorités locales, 11 parcs nationaux dans 8 pays d’Afrique, dont le parc national d’Odzala-Kokoua au Congo Brazzaville.</p>

<p class="lead">Ce parc de 13 500 km² (plus grand que la région Ile-de-France !), situé au cœur de la deuxième forêt tropicale du monde, abrite une biodiversité remarquable – mammifère, oiseaux, reptiles, plantes.  De nombreuses espèces sont aujourd’hui menacées, comme par exemple, les éléphants, les chimpanzés et les gorilles. En travaillant en étroite collaboration avec les communautés locales, plusieurs projets sont mis en œuvre pour assurer la durabilité à long terme du parc et diminuer le braconnage.</p>

							</div>
							<div class="tab-pane fade" id="tab-bottom2">
								<p class="lead">Amélie Romain a été recrutée par African Parks en tant que primatologue dans le cadre du lancement d’un programme d’habituation des gorilles des plaines de l’Ouest, afin de développer le tourisme et la recherche scientifique. Basée pendant plusieurs mois dans un camp de terrain rudimentaire (ni eau courante ni électricité), avec une équipe de pisteurs locaux – souvent d’anciens braconniers – nous suivions quotidiennement un groupe de gorilles, vivants dans une zone de forêt dense et de savane.</p>
