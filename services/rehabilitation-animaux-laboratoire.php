						<div class="col-md-12">

						<p>La réhabilitation animale s’inscrit dans une <b>démarche éthique et légale</b> en France : <b>il s’agit de procurer une retraite à des animaux sains en fin de protocole expérimental en alternative à l’euthanasie.</b></p> 
						<p>La réhabilitation est la suite logique du principe des 3R (Russel et Burch, 1959) qui vise à limiter l’expérimentation animale (grâce à des alternatives de « remplacement », « raffinement », « réduction » du nombre d’animaux utilisés en recherche). La réhabilitation des animaux de laboratoire est autorisée par les textes, notamment par les articles R 214-87 et 89 du Code Rural. Elle est intégrée en tant que 4ème R à la directive européenne sur l’expérimentation animale (2010/63/UE ; droit français : 2013-118) où il est précisé que : </p>


						<ul>
						<li> les grands singes sont exclus de l’expérimentation animale.</li>
						<li> les céphalopodes sont des animaux sensibles et pouvant ressentir la douleur.</li> 
						<li> la réutilisation d’animaux est autorisée dans le cadre d’études scientifiques.</li>
						<li> la réhabilitation est recommandée, en tant que 4ème R, pour les animaux sains arrivant en fin de protocole.</li>
						</ul>

				


						<center><img style="max-width: 70%;" src="http://www.akongo.fr/img/rehab/rehab1.jpg" class="img-responsive" alt="Animal de laboratoire réhabilité"></center>
						</br>
				
				

						<p>En France, depuis 2005, <a href="http://www.graal-defenseanimale.org">l’association GRAAL (Groupement de réflexion et d’action pour l’animal) </a>, assure ce service original, légal et unique aux unités de recherche françaises et européennes. Cela concerne toutes les espèces animales, et notamment les chiens, chats, chevaux, rongeurs, primates, animaux de ferme, oiseaux et poissons.</p>

						<p>Vecteur de communication entre le grand public et les scientifiques, cette démarche a reçu le soutien aussi bien de la <a href="https://www.la-spa.fr/">SPA (Société de Protection des Animaux) </a> que de la <a href="http://www.sfeca.fr/">SFECA (Société Française pour l’Étude du Comportement Animal) </a>.</p>



